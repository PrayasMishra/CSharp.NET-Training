public class ArithmeticOps
{
	public int Add(int x, int y)
	{
		return x+y;
	}

	public int Subtract(int x, int y)
	{
		return x-y;
	}

	public decimal Multiply(int x, int y)
	{
		return x*y;
	}
	
	public decimal Divide(int x, int y)
	{
		return x/y;
	}

	public int Modulo(int x, int y)
	{
		return x%y;
	}
}